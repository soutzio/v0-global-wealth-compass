// Function to get city data by code
export function getCityData(cityCode: string) {
  return cities.find((city) => city.code === cityCode) || null
}

// Function to get cities by country code
export function getCitiesByCountry(countryCode: string) {
  return cities.filter((city) => city.countryCode === countryCode)
}

// Function to get cities by region
export function getCitiesByRegion(countryCode: string, region: string) {
  return cities.filter((city) => city.countryCode === countryCode && city.region === region)
}

// Function to get top cities by median income
export function getTopCitiesByIncome(countryCode: string, limit = 5) {
  return getCitiesByCountry(countryCode)
    .sort((a, b) => b.medianIncome - a.medianIncome)
    .slice(0, limit)
}

// Function to get cities by cost of living (ascending)
export function getCitiesByCostOfLiving(countryCode: string, limit = 5) {
  return getCitiesByCountry(countryCode)
    .filter((city) => city.costOfLivingIndex !== undefined)
    .sort((a, b) => (a.costOfLivingIndex || 0) - (b.costOfLivingIndex || 0))
    .slice(0, limit)
}

// Function to calculate city-specific percentile
export function calculateCityPercentile(income: number, cityCode: string, countryCode: string) {
  const cityData = getCityData(cityCode)

  if (!cityData) {
    return null
  }

  // Calculate ratio of income to median income
  const ratio = income / cityData.medianIncome

  // Convert ratio to percentile (more accurate calculation)
  // This is a simplified model - in reality, income distributions follow more complex patterns
  if (ratio <= 0.25) return 25
  if (ratio <= 0.5) return 40
  if (ratio <= 0.75) return 45
  if (ratio <= 1.0) return 50
  if (ratio <= 1.25) return 60
  if (ratio <= 1.5) return 70
  if (ratio <= 2.0) return 80
  if (ratio <= 3.0) return 90
  if (ratio <= 5.0) return 95
  return 99
}
